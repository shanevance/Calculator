    private void jButton24ActionPerformed(java.awt.event.ActionEvent evt) {                                          
    
    	String exp = jTextField1.getText();

    	try {
		
    		exp = Calculator.convertToProperExpression(exp);

    	} catch (IndexOutOfBoundsException e) {
			
			jTextField1.setText(e.getMessage());
			
		}
		
		if (Calculator.isEquation(exp)) {

			try {
	
				BigDecimal result = Calculator.eval(exp);
				jTextField1.setText(result.toString());
	
			} catch (NumberFormatException e) {

				jTextField1.setText("Error: invalid input");

			} catch (EmptyStackException e) {

				jTextField1.setText("Error: invalid input");

			} catch (UnsupportedOperationException e) {
	
				jTextField1.setText(e.getMessage());
	
			} catch (ArithmeticException e) {
	
				jTextField1.setText(e.getMessage());

			} catch (ArrayIndexOutOfBoundsException e) {
					
				jTextField1.setText("Error: invalid input");
				
			} catch (IndexOutOfBoundsException e) {
				
				jTextField1.setText("Error: invalid input");
				
			}

		} else if (exp.length() == 0) {

			jTextField1.setText("Nothing to evaluate");

		} else {
	
			jTextField1.setText("Error: invalid input");

		}
			
		
		
    }                                                                                                                      

